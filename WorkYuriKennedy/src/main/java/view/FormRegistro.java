private void atualizar() throws IOException {
        String ID, record2;
        File db = new File(pathFile);
        File tempDB = new File(pathFileTemp);
        BufferedReader br2;
        try (BufferedWriter bw = new BufferedWriter(new FileWriter(tempDB))) {
            ID = "RM-"+tf_rm.getText();
            br2 = new BufferedReader(new FileReader(db));
            while ((record2 = br2.readLine()) != null) {
                if (record2.contains(ID)) {
                    bw.write("RM-"+tf_rm.getText()+";" + tf_paciente.getText() + ";" + tf_endereco.getText());
                } else {
                    bw.write(record2);
                }
                bw.flush();
                bw.newLine();
            }
        }
        br2.close();
        db.delete();
        tempDB.renameTo(db);
    }
    
private void salvarEatualizar(){
        if (btn_salvar.getText().equals("Salvar")) {
            String save = "\n" + "RM-" + tf_rm.getText() + ";" + tf_paciente.getText() + ";" + tf_endereco.getText();
            try (BufferedWriter bw = new BufferedWriter(new FileWriter(pathFile, true))) {
                bw.write(save);
                bw.flush();
                bw.newLine();
                bw.close();
                limpartela();
                mostrardados();
                dialog("Dados Salvos com Sucesso");
            } catch (IOException e) {
            }
        } else {
            try {
                atualizar();
                limpartela();
                mostrardados();
                btn_deletar.setEnabled(false);
                btn_salvar.setText("Salvar");
                dialog("Dados alterados com sucesso");
            } catch (IOException e) {
            }
        }
    }
    

